#cacheSolve takes what is returned from the makeCacheMatrix function in order to get (set) the inverse of a chaced (new) matrix that is within the makeCacheMatrix environment
cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        m <- x$getinverse()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinverse(m)
        m
}
